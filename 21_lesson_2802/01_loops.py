"""
range(b) - последовательность 0...b - 1
range(a, b) - последовательность a...b - 1
range(a, b, x) - последовательность a...b - 1, шаг x
"""
for i in range(5):  # 0 1 2 3 4
    print(i)

for j in range(10, 16):  # 10 11 12 13 14 15
    print(j)

for k in range(1, 10, 2):  # 1 3 5 7 9
    print(k)

for l in 'спасибо':
    print(l)

