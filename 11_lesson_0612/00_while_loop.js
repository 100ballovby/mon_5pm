let x = 1;

while (true) {  // повторять всегда
    //x = x + 1 инкремент (увеличение с присваиванием)
    x += 1  // +=, *=, **=
    console.log(x)
    if (x >= 10) {  // условие выхода из цикла
        break   // принудительный выход из цикла
    }
}
/* Декремент - уменьшение с присваиванием
-=, /=, %=  */